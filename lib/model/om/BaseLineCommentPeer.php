			if ($value) {
				$selectCriteria->add(LineCommentPeer::ID, $value, $comparison);
			} else {
				$selectCriteria->setPrimaryTableName(LineCommentPeer::TABLE_NAME);
			}

		} else { // $values is LineComment object
			$criteria = $values->buildCriteria(); // gets full criteria
